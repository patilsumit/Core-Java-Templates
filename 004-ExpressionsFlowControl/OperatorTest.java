public class OperatorTest{



	public static void main(String[] args){

		// logical or boolean operators and shor-circuit operators example example

		boolean x = true;
		boolean y = false;

		System.out.println("x & y : " + (x & y));// logical operator or boolean operator

		System.out.println("x && y : " + (x && y));// short circuit logical operator

		System.out.println("x | y : " + (x | y));// logical operator or boolean operator
		System.out.println("x || y: " + (x || y));// short circuit logical operator
		System.out.println("x ^ y : " + (x ^ y));// logical operators ort boolen operators
		System.out.println("!x : " + (!x));// logical operators or boolean operators

	
		
	}

}
